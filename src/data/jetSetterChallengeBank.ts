function getTripType(cardType: JetSetterCardType): TripType {
  if (cardType === 'Crew') return 'Crew Challenge';
  if (cardType === 'Proof' || cardType === 'Receipt') return 'Evidence Challenge';
  return 'Field Challenge';
}

